
public class Resolution{

	private int en;

	private int boy;

	public Resolution(int en, int boy){
		this.en 	= en;
		this.boy 	= boy;
	}

	// get method for en
	public int getEn(){
		return en;
	}
		// set method for en
	public void setEn(int en){
		this.en = en;
	}
		// get method for boy
	public int getBoy(){
		return boy;
	}
			// set method for boy
	public void setBoy(int boy){
		this.boy = boy;
	}

}